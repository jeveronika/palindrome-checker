# Palindrome checker with user input. Works for both numbers (number as an input is a string) and text (string)
user_input = input("Is this a palindrome: ").casefold()
reverse_input = reversed(user_input)
if list(user_input) == list(reverse_input):
   print(f"{user_input} is a palindrome")
else:
   print(f"{user_input} is not a palindrome")


# Palindrome checker without user input. Works for strings, not for number (integers):
str_p = "1234"
reverse_input = reversed(str_p)
if list(str_p) == list(reverse_input):
   print(f"{str_p} is a palindrome")
else:
   print(f"{str_p} is not a palindrome")

# Palindrome checker without user input. Works for (integers):
num = 1212
num = str(num)
reverse_num = reversed(num)
if list(num) == list(reverse_num):
   print(f"{num} is a palindrome")
else:
   print(f"{num} is not a palindrome")
